                <img src="http://www.frutas-hortalizas.com/img/fruites_verdures/presentacio/2.jpg" alt="">
                <p>El arándano es un fruto que crece silvestre en zonas frescas del hemisferio norte. Es una baya globosa de color negro azulado que mide unos 6mm de diámetro. Se consume sobre todo en mermeladas, tartas o como acompañamiento de diversos platos. Es un alimento rico en vitaminas que además aporta pocas calorías al organismo.</p>
