                <p>La naranja es un fruto redondo, color naranja, consumido mayoritariamente en invierno. La pulpa del interior es también anaranjada y está formada por pequeñas bolsitas llenas de zumo. La naranja se usa para consumo en fresco y, para la industria, principalmente en zumo.</p>
            </td>
