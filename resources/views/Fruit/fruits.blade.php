                <h3>AGUACATE</h3>
                <img src="http://www.frutas-hortalizas.com/img/fruites_verdures/presentacio/86.jpg" alt="">
                <p>El aguacate es un fruto exótico carnoso que se obtiene del árbol tropical del mismo nombre. En algunas partes de América del Sur se conoce como Palta. Presenta unas dimensiones de 5-6 cm de longitud. El peso normal oscila entre 200-400 g, aunque pueden encontrarse piezas de hasta 2 kg de peso. La corteza es gruesa y dura de color verde cuyo tono depende de la variedad.</p>
            </td>
