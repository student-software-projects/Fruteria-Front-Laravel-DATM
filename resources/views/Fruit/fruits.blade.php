                <h3>UVA</h3>
                <img src="http://www.frutas-hortalizas.com/img/fruites_verdures/presentacio/8.jpg" alt="">
                <p>La uva es un fruta que crece en racimos apretados. Su pulpa es blanca o púrpura y de sabor dulce. Se consume como fruta fresca o zumo, aunque su utilidad principal es la obtención de vinos. También se realizan conservas con ella. Contiene diversos minerales y vitaminas, y se piensa que tiene poderes antioxidantes y anticancerígenos.</p>
            </td>
            <td>
                <h3>SANDÍA</h3>
                <img src="http://www.frutas-hortalizas.com/img/fruites_verdures/presentacio/30.jpg" alt="">
                <p>La sandía es un fruto grande y de forma más o menos esférica que suele consumirse cruda como postre. Su pulpa es de color rojizo o amarillento y de sabor dulce. Resulta un alimento muy refrescante que aporta muy pocas calorías al organismo, también aporta algunas vitaminas y minerales.</p>
            </td>
